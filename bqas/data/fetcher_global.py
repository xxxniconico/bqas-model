def _get_conn(db_name="bqas_global.db"):
    """Get SQLite connection for global data."""
    DB_DIR.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(str(DB_DIR / db_name))
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("PRAGMA synchronous=NORMAL")
    return conn


def _init_tables(conn, market: str):
    """Create tables for a market (us or hk)."""
    m = market  # 'us' or 'hk'
    conn.executescript(f"""
        CREATE TABLE IF NOT EXISTS stock_info_{m} (
            code TEXT PRIMARY KEY, name TEXT, industry TEXT,
            market_cap REAL, country TEXT, sector TEXT,
            total_shares REAL, listing_date TEXT, currency TEXT
        );
        CREATE TABLE IF NOT EXISTS income_{m} (
            code TEXT, report_period TEXT, revenue REAL,
            operating_profit REAL, net_income REAL,
            interest_expense REAL, ebit REAL,
            PRIMARY KEY(code, report_period)
        );
        CREATE TABLE IF NOT EXISTS balance_{m} (
            code TEXT, report_period TEXT, total_assets REAL,
            total_liabilities REAL, equity REAL, goodwill REAL,
            cash REAL, inventory REAL, accounts_receivable REAL,
            long_term_invest REAL, short_term_debt REAL, long_term_debt REAL,
            PRIMARY KEY(code, report_period)
        );
        CREATE TABLE IF NOT EXISTS cashflow_{m} (
            code TEXT, report_period TEXT, operating_cf REAL,
            capex REAL, financing_cf REAL,
            PRIMARY KEY(code, report_period)
        );
        CREATE TABLE IF NOT EXISTS quotes_{m} (
            code TEXT, trade_date TEXT, close REAL, pb REAL,
            market_cap REAL, volume REAL,
            PRIMARY KEY(code, trade_date)
        );
    """)


def fetch_stock_data(market: str, symbols: list[str], force: bool = False):
    """Fetch full financial data for a list of stocks via yfinance.

    Args:
        market: 'us' or 'hk'
        symbols: list of ticker symbols (e.g., 'AAPL' or '0700.HK')
        force: re-fetch even if already cached
    """
    import yfinance as yf

    conn = _get_conn()
    _init_tables(conn, market)

    total = len(symbols)
    success = 0
    t0 = time.time()

    for i, sym in enumerate(symbols):
        # Skip if already fetched
        if not force:
            existing = conn.execute(
                f"SELECT COUNT(*) FROM stock_info_{market} WHERE code=?",
                (sym,)
            ).fetchone()[0]
            if existing > 0:
                success += 1
                continue

        try:
            ticker = yf.Ticker(sym)
            info = ticker.info
            if not info or info.get('trailingPegRatio') is None and not info.get('marketCap'):
                time.sleep(0.5)
                continue

            # ── Stock Info ──
            conn.execute(
                f"INSERT OR REPLACE INTO stock_info_{market} VALUES(?,?,?,?,?,?,?,?,?)",
                (sym,
                 info.get('longName') or info.get('shortName', sym),
                 info.get('industry', ''),
                 info.get('marketCap', 0),
                 info.get('country', ''),
                 info.get('sector', ''),
                 info.get('sharesOutstanding', 0),
                 datetime.now().strftime('%Y-%m-%d'),
                 info.get('currency', 'USD' if market == 'us' else 'HKD'))
            )

            # ── Income Statement ──
            income = ticker.financials
            if income is not None and not income.empty:
                for col in income.columns:
                    period = str(col.date()) if hasattr(col, 'date') else str(col)[:10]
                    try:
                        conn.execute(
                            f"INSERT OR REPLACE INTO income_{market} VALUES(?,?,?,?,?,?,?)",
                            (sym, period,
                             float(income.loc['Total Revenue', col]) if 'Total Revenue' in income.index else 0,
                             float(income.loc['Operating Income', col]) if 'Operating Income' in income.index else 0,
                             float(income.loc['Net Income', col]) if 'Net Income' in income.index else 0,
                             abs(float(income.loc['Interest Expense', col])) if 'Interest Expense' in income.index else 0,
                             float(income.loc['EBIT', col]) if 'EBIT' in income.index else 0)
                        )
                    except (KeyError, TypeError, ValueError):
                        continue

            # ── Balance Sheet ──
            balance = ticker.balance_sheet
            if balance is not None and not balance.empty:
                for col in balance.columns:
                    period = str(col.date()) if hasattr(col, 'date') else str(col)[:10]
                    try:
                        def _b(key, default=0):
                            return float(balance.loc[key, col]) if key in balance.index else default
                        conn.execute(
                            f"INSERT OR REPLACE INTO balance_{market} VALUES(?,?,?,?,?,?,?,?,?,?,?,?)",
                            (sym, period,
                             _b('Total Assets'), _b('Total Liabilities Net Minority Interest'),
                             _b('Stockholders Equity'), _b('Goodwill And Other Intangible Assets'),
                             _b('Cash And Cash Equivalents'), _b('Inventory'),
                             _b('Accounts Receivable'), _b('Long Term Investments'),
                             _b('Current Debt'), _b('Long Term Debt'))
                        )
                    except (KeyError, TypeError, ValueError):
                        continue

            # ── Cash Flow ──
            cashflow = ticker.cashflow
            if cashflow is not None and not cashflow.empty:
                for col in cashflow.columns:
                    period = str(col.date()) if hasattr(col, 'date') else str(col)[:10]
                    try:
                        def _c(key, default=0):
                            return float(cashflow.loc[key, col]) if key in cashflow.index else default
                        conn.execute(
                            f"INSERT OR REPLACE INTO cashflow_{market} VALUES(?,?,?,?,?)",
                            (sym, period,
                             _c('Operating Cash Flow'),
                             abs(_c('Capital Expenditure')),
                             _c('Financing Cash Flow'))
                        )
                    except (KeyError, TypeError, ValueError):
                        continue

            success += 1
            time.sleep(0.3)  # rate limit

        except Exception as e:
            logger.warning(f"Failed {sym}: {e}")
            continue

        if (i + 1) % 10 == 0:
            conn.commit()
            elapsed = time.time() - t0
            rate = (i + 1) / elapsed if elapsed > 0 else 0
            eta = (total - i - 1) / rate if rate > 0 else 0
            logger.info(f"  {i+1}/{total} ({success} ok) | {rate:.1f}/s | ETA {eta:.0f}s")

    conn.commit()
    conn.close()
    elapsed = time.time() - t0
    logger.info(f"Done {market}: {success}/{total} stocks in {elapsed:.0f}s")


def build_global_cache(us=True, hk=True, force=False):
    """Build full global cache for US and HK markets."""
    if us:
        logger.info(f"Fetching {len(SP500_SYMBOLS)} US stocks...")
        fetch_stock_data('us', SP500_SYMBOLS, force=force)
    if hk:
        logger.info(f"Fetching {len(HK_SYMBOLS)} HK stocks...")
        fetch_stock_data('hk', HK_SYMBOLS, force=force)


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO, format='%(asctime)s %(message)s')
    build_global_cache(us=True, hk=True, force=True)
